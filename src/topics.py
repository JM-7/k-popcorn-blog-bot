TOPIC_POOL = {
    "알고 먹자": FOOD_TOPICS,
    "전쟁이야기": WAR_TOPICS,
}


def pick_category() -> str:
    """카테고리를 랜덤으로 하나 선택합니다."""
    return random.choice(config.CATEGORIES)


def pick_topic(category: str) -> str:
    """주어진 카테고리의 주제 후보 중 하나를 랜덤으로 선택합니다."""
    pool = TOPIC_POOL.get(category)
    if not pool:
        raise ValueError(f"알 수 없는 카테고리: {category}")
    return random.choice(pool)


def pick_category_and_topic() -> tuple[str, str]:
    """카테고리와 주제를 한 번에 선택합니다."""
    category = pick_category()
    topic = pick_topic(category)
    return category, topic
